Cette semaine, notre rencontre prendra exceptionnellement la forme d’un concours d’entraînement.
L’objectif est de se mettre dans des conditions similaires à celles d’un concours de programmation.
Pendant deux heures, vous devrez ◊em{résoudre le plus de problèmes possible} parmi 5 problèmes qui seront dévoilés le jour même.
Ces problèmes porteront sur les thématiques vues pendant les semaines précédentes (géométrie, chaînes de caractères, graphes, programmation dynamique).

Le concours aura lieu sur Kattis.
Vous pourrez soumettre des tentatives entre 17:35 et 19:35.
Le classement se fera en ordre décroissant selon le nombre de problèmes résolus, puis, en cas d’égalité, en ordre croissant selon la somme du temps pris à résoudre les problèmes, plus une pénalité de 20 minutes pour chaque erreur.

