Cette semaine marque le début des rencontres d’été du club.
Les problèmes de cette semaine font appel à diverses techniques algorithmiques.
Ils ont été choisis pour leur thématique en référence au ◊out-link["https://radar-iro.github.io"]{club RADAR} nouvellement formé pendant l’hiver.

◊link-h2["problems"]{Problèmes}

Les problèmes suivants sont triés par ordre croissant de difficulté.

◊ol[#:class "long-list"]{
    ◊li{◊out-link["https://open.kattis.com/problems/remorse"]{“ReMorse”} — Trouvez une façon d’attribuer une séquence de points et tirets à chaque lettre pour obtenir le codage le plus court possible pour un mot donné.}
    ◊li{◊out-link["https://open.kattis.com/problems/countingstars"]{“Counting Stars”} — Combien d’étoiles sont visibles dans une image du ciel?}
    ◊li{◊out-link["https://open.kattis.com/problems/communicationssatellite"]{“Communication Satellite”} — Placez des supports à des endroits appropriés pour soutenir la structure d’un satellite en minimisant la quantité de matériau utilisé.}
    ◊li{◊out-link["https://open.kattis.com/problems/radiotransmission"]{“Radio Transmission”} — Identifiez un message à partir d’un extrait de communication dans lequel le message est répété plusieurs fois.}
    ◊li{◊out-link["https://open.kattis.com/problems/radar"]{“Radar”} — Utilisez la phase de trois signaux reçus d’une tour radio pour obtenir une borne inférieure sur la distance qui vous sépare.}
}
