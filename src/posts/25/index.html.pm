Les problèmes de cette semaine sont tirés du concours régional ◊out-link["https://2022.nwerc.eu/"]{NWERC 2022} de l’ICPC, qui s’est tenu en novembre dernier.
La thématique des problèmes alterne principalement entre géométrie et combinatoire.

◊link-h2["problems"]{Problèmes}

Les problèmes suivants sont triés par ordre croissant de difficulté.

◊ol[#:class "long-list"]{
    ◊li{◊out-link["https://open.kattis.com/problems/bottleflip"]{“Bottle Flip”} — À quel niveau doit-on remplir une bouteille d’eau afin que son centre de masse soit le plus bas possible?}
    ◊li{◊out-link["https://open.kattis.com/problems/circularcaramelcookie"]{“Circular Caramel Cookie”} — Calculez la dimension minimale d’un disque pour qu’il puisse contenir ◊math{s} carrés d’aire unitaire.}
    ◊li{◊out-link["https://open.kattis.com/problems/highqualitytree"]{“High-quality Tree”} — Combien de fois au moins faut-il retirer une feuille d’un arbre binaire pour le rendre équilibré?}
    ◊li{◊out-link["https://open.kattis.com/problems/eta"]{“ETA”} — Construisez un graphe dont la distance moyenne à un sommet donné égale une quantité voulue.}
    ◊li{◊out-link["https://open.kattis.com/problems/lastguess"]{“Last Guess”} — Résolvez le jeu Wordle.}
}
